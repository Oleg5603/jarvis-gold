def find_topics(text: str) -> list[str]:
    """Находит темы по ключевым словам в тексте."""
    text_lower = text.lower()
    found = []
    for topic, keywords in TRIGGERS.items():
        for kw in keywords:
            if kw in text_lower and topic not in found:
                found.append(topic)
    return found


def get_content(topics: list[str]) -> dict:
    """Возвращает вопросы и рекомендации по найденным темам."""
    questions = []
    recommendations = []
    for topic in topics:
        if topic in KNOWLEDGE_BASE:
            questions.extend(KNOWLEDGE_BASE[topic]["questions"])
            recommendations.extend(KNOWLEDGE_BASE[topic]["recommendations"])
    return {
        "questions": questions[:6],
        "recommendations": recommendations[:6],
    }
